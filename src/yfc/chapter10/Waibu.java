package yfc.chapter10;

public class Waibu {

    class Neibu {
        String name;

        public Neibu(String name) {
            this.name = name;
        }

        @Override
        public String toString() {
            return "Neibu{" +
                    "name='" + name + '\'' +
                    '}';
        }
    }
}
